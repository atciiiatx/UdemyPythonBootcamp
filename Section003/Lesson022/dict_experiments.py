"""Dictionary experiments."""

price_lookup = {'apple': 2.99, 'orange': 1.99, 'milk': 4.51}
item = 'milk'
print(f'The price of {item} is {price_lookup[item]}')
print(f'price_lookup keys = {price_lookup.keys()}')
print(f'price_lookup values = {price_lookup.values()}')
print(f'price_lookup items = {price_lookup.items()}')
